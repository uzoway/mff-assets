var prefersReducedMotion = window.matchMedia('(prefers-reduced-motion: reduce)').matches;
var CHUNK_SIZE = 18;

var state = {
  ga: { rendered: 0, data: galleryData.ga },
  vip: { rendered: 0, data: galleryData.vip }
};

var currentTab = 'ga';
var currentLightboxIndex = 0;
var lastFocusedElement = null;

var lazyObserver = new IntersectionObserver(function (entries) {
  entries.forEach(function (entry) {
    if (!entry.isIntersecting) return;
    var img = entry.target.querySelector('img[data-src]');
    if (img) {
      img.src = img.dataset.src;
      img.removeAttribute('data-src');
    }
    lazyObserver.unobserve(entry.target);
  });
}, { rootMargin: '400px' });

var scrollObserver = new IntersectionObserver(function(entries) {
  entries.forEach(function(entry) {
    if (entry.isIntersecting) {
      var tabId = entry.target.getAttribute('data-tab-marker');
      scrollObserver.unobserve(entry.target);
      loadNextChunk(tabId);
    }
  });
}, { rootMargin: '600px' });

function loadNextChunk(tabId) {
  var tabState = state[tabId];
  if (tabState.rendered >= tabState.data.length) return;

  var container = document.querySelector('[data-tab-pane="' + tabId + '"] .gallery_list');
  var toRender = tabState.data.slice(tabState.rendered, tabState.rendered + CHUNK_SIZE);
  var fragment = document.createDocumentFragment();

  toRender.forEach(function (item, chunkIndex) {
    var globalIndex = tabState.rendered + chunkIndex;
    var wrapper = document.createElement('div');
    wrapper.className = 'gallery_image-wrapper';
    wrapper.setAttribute('role', 'button');
    wrapper.setAttribute('tabindex', '0');
    wrapper.setAttribute('aria-label', 'View larger image');
    wrapper.setAttribute('data-index', globalIndex);
    
    // SAFARI FIX: Forces iOS to recognize taps on this div
    wrapper.style.cursor = 'pointer'; 
    wrapper.style.touchAction = 'manipulation';

    var img = document.createElement('img');
    img.dataset.src = item.src;
    img.alt = item.alt || '';
    img.className = 'gallery_image';
    img.draggable = false;
    
    // SAFARI FIX: Forces image processing off the Main Thread so the UI never freezes
    img.decoding = 'async'; 
    
    img.onload = function () { this.parentElement.classList.add('is-loaded'); };

    wrapper.appendChild(img);
    fragment.appendChild(wrapper);
    lazyObserver.observe(wrapper);
  });

  container.appendChild(fragment);
  tabState.rendered += toRender.length;

  var lastChild = container.lastElementChild;
  if (lastChild && tabState.rendered < tabState.data.length) {
    lastChild.setAttribute('data-tab-marker', tabId);
    scrollObserver.observe(lastChild);
  }
}

function initGallery() {
  var tabBtns = document.querySelectorAll('[data-tab-btn]:not([data-tab-btn="highlight"])');
  var tabPanes = document.querySelectorAll('[data-tab-pane]');
  var highlight = document.querySelector('[data-tab-btn="highlight"]');
  var galleryContainer = document.querySelector('.gallery_tabs-content');
  var lightboxWrap = document.getElementById('lightbox-wrap');
  var lightboxImage = document.getElementById('lightbox-image');
  var lightboxClose = document.getElementById('lightbox-close');
  var lightboxOverlay = document.getElementById('lightbox-overlay');

  var dragging = false;
  var didDrag = false;
  var startX = 0;
  var startY = 0;
  var lastDx = 0;
  var SWIPE_THRESHOLD = 60;
  var TAP_SLOP = 6;

  if (lightboxImage) lightboxImage.setAttribute('draggable', 'false');

  tabPanes.forEach(function (pane) {
    pane.style.display = 'block';
    gsap.set(pane, { autoAlpha: 0, zIndex: 1 });
  });
  var initialPane = document.querySelector('[data-tab-pane="ga"]');
  gsap.set(initialPane, { autoAlpha: 1, zIndex: 2 });
  gsap.set(highlight, { xPercent: 0 });

  function switchPane(targetTab) {
    tabPanes.forEach(function (pane) {
      gsap.set(pane, { autoAlpha: 0, zIndex: 1 });
    });
    var targetPane = document.querySelector('[data-tab-pane="' + targetTab + '"]');
    gsap.set(targetPane, { autoAlpha: 1, zIndex: 2 });
    return targetPane;
  }

  tabBtns.forEach(function (btn, index) {
    btn.style.touchAction = 'manipulation';
    btn.addEventListener('click', function () {
      var targetTab = this.getAttribute('data-tab-btn');
      if (targetTab === currentTab) return;

      tabBtns.forEach(function (b) { b.classList.remove('is-active'); });
      this.classList.add('is-active');
      gsap.to(highlight, { xPercent: index * 100, duration: 0.3, ease: 'power2.out' });

      var newPane = switchPane(targetTab);
      currentTab = targetTab;

      if (state[targetTab].rendered === 0) {
        loadNextChunk(targetTab);
      }

      gsap.killTweensOf('.gallery_image-wrapper');

      var newItems = Array.from(newPane.querySelectorAll('.gallery_image-wrapper'));
      if (!prefersReducedMotion && newItems.length > 0) {
        gsap.fromTo(newItems.slice(0, 24),
          { y: 20, opacity: 0 },
          { y: 0, opacity: 1, duration: 0.3, stagger: 0.02, ease: 'power2.out' }
        );
      }
    });
  });

  function openLightbox(wrapper) {
    var index = parseInt(wrapper.getAttribute('data-index'), 10);
    if (isNaN(index)) return;

    currentLightboxIndex = index;
    var itemData = state[currentTab].data[index];

    lastFocusedElement = wrapper;
    lightboxImage.src = itemData.src;
    lightboxImage.alt = itemData.alt || '';
    document.body.style.overflow = 'hidden';

    if (prefersReducedMotion) {
      gsap.set(lightboxWrap, { autoAlpha: 1 });
      gsap.set(lightboxImage, { scale: 1 });
      lightboxClose.focus();
    } else {
      gsap.to(lightboxWrap, {
        autoAlpha: 1,
        duration: 0.25,
        ease: 'power2.out',
        onComplete: function () { lightboxClose.focus(); }
      });
      gsap.fromTo(lightboxImage,
        { scale: 0.96 },
        { scale: 1, duration: 0.3, ease: 'back.out(1.2)' }
      );
    }
  }

  function navigateLightbox(direction) {
    var dataArray = state[currentTab].data;
    currentLightboxIndex += direction;

    if (currentLightboxIndex < 0) {
      currentLightboxIndex = dataArray.length - 1;
    } else if (currentLightboxIndex >= dataArray.length) {
      currentLightboxIndex = 0;
    }

    if (state[currentTab].rendered <= currentLightboxIndex) {
      loadNextChunk(currentTab);
    }

    var nextItem = dataArray[currentLightboxIndex];
    var outX = direction === 1 ? -60 : 60;
    var inX = direction === 1 ? 60 : -60;

    if (prefersReducedMotion) {
      lightboxImage.src = nextItem.src;
      lightboxImage.alt = nextItem.alt || '';
      gsap.set(lightboxImage, { x: 0, opacity: 1 });
      return;
    }

    gsap.timeline()
      .to(lightboxImage, { x: outX, opacity: 0, duration: 0.15, ease: 'power1.in' })
      .call(function () {
        lightboxImage.src = nextItem.src;
        lightboxImage.alt = nextItem.alt || '';
      })
      .fromTo(lightboxImage,
        { x: inX, opacity: 0 },
        { x: 0, opacity: 1, duration: 0.2, ease: 'power1.out' }
      );
  }

  galleryContainer.addEventListener('click', function (e) {
    var clickedWrapper = e.target.closest('.gallery_image-wrapper');
    if (clickedWrapper) openLightbox(clickedWrapper);
  });

  galleryContainer.addEventListener('keydown', function (e) {
    if (e.key === 'Enter' || e.key === ' ') {
      var focusedWrapper = e.target.closest('.gallery_image-wrapper');
      if (focusedWrapper) {
        e.preventDefault();
        openLightbox(focusedWrapper);
      }
    }
  });

  function onPointerDown(e) {
    if (e.target.closest('.lightbox_close') || e.target === lightboxOverlay) return;
    dragging = true;
    didDrag = false;
    startX = e.clientX;
    startY = e.clientY;
    lastDx = 0;

    lightboxImage.style.cursor = 'grabbing';
    gsap.killTweensOf(lightboxImage, 'x');

    document.addEventListener('pointermove', onPointerMove);
    document.addEventListener('pointerup', onPointerUp);
    document.addEventListener('pointercancel', onPointerUp);
  }

  function onPointerMove(e) {
    if (!dragging) return;
    var dx = e.clientX - startX;
    var dy = e.clientY - startY;

    if (!didDrag && (Math.abs(dx) > TAP_SLOP || Math.abs(dy) > TAP_SLOP)) {
      didDrag = true;
    }

    if (Math.abs(dx) >= Math.abs(dy)) {
      lastDx = dx;
      gsap.set(lightboxImage, { x: dx });
    }
  }

  function onPointerUp() {
    if (!dragging) return;
    dragging = false;
    lightboxImage.style.cursor = 'grab';

    document.removeEventListener('pointermove', onPointerMove);
    document.removeEventListener('pointerup', onPointerUp);
    document.removeEventListener('pointercancel', onPointerUp);

    if (Math.abs(lastDx) > SWIPE_THRESHOLD) {
      navigateLightbox(lastDx < 0 ? 1 : -1);
    } else if (didDrag) {
      gsap.to(lightboxImage, { x: 0, duration: 0.2, ease: 'power2.out' });
    }
  }

  lightboxImage.addEventListener('pointerdown', onPointerDown);
  lightboxImage.addEventListener('click', function (e) { e.stopPropagation(); });

  function closeLightbox() {
    if (prefersReducedMotion) {
      gsap.set(lightboxWrap, { autoAlpha: 0 });
      lightboxImage.src = '';
      gsap.set(lightboxImage, { x: 0 });
      document.body.style.overflow = '';
      if (lastFocusedElement) lastFocusedElement.focus();
      return;
    }

    gsap.to(lightboxWrap, {
      autoAlpha: 0,
      duration: 0.25,
      ease: 'power2.inOut',
      onComplete: function () {
        lightboxImage.src = '';
        gsap.set(lightboxImage, { x: 0 });
        document.body.style.overflow = '';
        if (lastFocusedElement) lastFocusedElement.focus();
      }
    });
  }

  lightboxClose.addEventListener('click', closeLightbox);
  lightboxOverlay.addEventListener('click', closeLightbox);

  lightboxWrap.addEventListener('keydown', function (e) {
    if (e.key === 'Escape' && gsap.getProperty(lightboxWrap, 'opacity') > 0) {
      closeLightbox();
      return;
    }

    if (e.key === 'ArrowRight') navigateLightbox(1);
    if (e.key === 'ArrowLeft') navigateLightbox(-1);

    if (e.key === 'Tab') {
      var focusableElements = lightboxWrap.querySelectorAll(
        'button, [href], input, select, textarea, [tabindex]:not([tabindex="-1"])'
      );
      if (!focusableElements.length) return;

      var firstElement = focusableElements[0];
      var lastElement = focusableElements[focusableElements.length - 1];

      if (e.shiftKey) {
        if (document.activeElement === firstElement) {
          lastElement.focus();
          e.preventDefault();
        }
      } else {
        if (document.activeElement === lastElement) {
          firstElement.focus();
          e.preventDefault();
        }
      }
    }
  });
}

document.addEventListener('DOMContentLoaded', function () {
  loadNextChunk('ga');
  initGallery();
});
